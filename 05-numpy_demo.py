# To demonstrate the basic functionality of the NumPy library, specifically creating arrays and calculating the mean.

# Import the NumPy library
import numpy as np

# Create a 1-dimensional array using NumPy
arr = np.array([1, 2, 3, 4, 5])

# Calculate and print the mean of the array
print("Mean:", np.mean(arr))






