# Variables and Data Types

# In Python, you can declare variables to store data of different types.

# Integer variable

age = 25

# Floating-point variable

height = 5.9

# String variable

name = "Alice"

# Boolean variable

is_student = True

# Print variables

print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is student?", is_student)
