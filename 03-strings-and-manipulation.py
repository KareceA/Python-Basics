# Introduction to Strings and String Manipulation
# Strings are sequences of characters. You can manipulate them in various ways.

# Creating strings
greeting = "Hello, "
name = "Alice"

# Concatenation
message = greeting + name

# String length
length = len(message)

# Slicing
substring = message[0:5]

print("Concatenated Message:", message)
print("Length of Message:", length)
print("Substring:", substring)
