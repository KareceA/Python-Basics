# Basic Operations and Arithmetic
# You can perform various arithmetic operations in Python.

# Addition
result = 10 + 5

# Subtraction
difference = 20 - 7

# Multiplication
product = 6 * 8

# Division
quotient = 15 / 3

# Modulo (remainder)
remainder = 20 % 6

print("Addition:", result)
print("Subtraction:", difference)
print("Multiplication:", product)
print("Division:", quotient)
print("Modulo:", remainder)
